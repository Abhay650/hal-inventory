--------------------------------------------------------
-- USERS TABLE
--------------------------------------------------------

CREATE TABLE USERS(
    USER_ID INT AUTO_INCREMENT PRIMARY KEY,
    EMPLOYEE_ID VARCHAR(20),
    EMPLOYEE_NAME VARCHAR(100),
    DEPARTMENT VARCHAR(50),
    USERNAME VARCHAR(50) UNIQUE,
    PASSWORD VARCHAR(100),
    EMAIL VARCHAR(100),
    PHONE VARCHAR(15)
);

