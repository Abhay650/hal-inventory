--------------------------------------------------------
-- STOCKS
--------------------------------------------------------

CREATE TABLE STOCKS(

STOCK_ID INT AUTO_INCREMENT PRIMARY KEY,

PART_ID VARCHAR(20),

CURRENT_STOCK INT,

MINIMUM_STOCK INT,

REORDER_STOCK INT,

SHELF_LIFE VARCHAR(30),

LAST_UPDATE DATE,

FOREIGN KEY(PART_ID)
REFERENCES COMPUTER_PARTS(PART_ID)

);

